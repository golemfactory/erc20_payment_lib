pub fn get_map_address_amounts() -> HashMap<String, String> {
    let public_keys = get_test_accounts()
        .iter()
        .map(|x| x.1)
        .collect::<Vec<&str>>();
    let amounts = get_test_accounts()
        .iter()
        .map(|x| x.2)
        .collect::<Vec<&str>>();

    public_keys
        .iter()
        .map(|x| x.to_lowercase())
        .zip(amounts.iter().map(|x| x.to_string()))
        .collect()
}
